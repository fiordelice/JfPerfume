const clearCartBtn = document.getElementById('clearCartBtn');
const checkoutBtn = document.getElementById('checkoutBtn');

let activeCategory = 'todas';
const cart = {};

const formatPrice = (value) =>
  value.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' });

function getFilteredProducts() {
  const query = searchInput.value.trim().toLowerCase();

  return products.filter((product) => {
    const matchesCategory = activeCategory === 'todas' || product.category === activeCategory;
    const matchesSearch = [product.name, product.description, product.category]
      .join(' ')
      .toLowerCase()
      .includes(query);

    return matchesCategory && matchesSearch;
  });
}

function renderProducts() {
  const filtered = getFilteredProducts();

  if (filtered.length === 0) {
    productsGrid.innerHTML = `
      <div class="empty-state">
        <h3>Nenhum perfume encontrado</h3>
        <p>Tente outro termo de busca ou selecione uma categoria diferente.</p>
      </div>
    `;
    return;
  }

  productsGrid.innerHTML = filtered
    .map(
      (product) => `
      <article class="product-card">
        <div class="product-card__image-wrap">
          <img class="product-card__image" src="${product.image}" alt="${product.name}" />
        </div>
        <div class="product-card__body">
          <span class="product-badge">${product.category}</span>
          <h3>${product.name}</h3>
          <p>${product.description}</p>
          <div class="product-footer">
            <span class="product-price">${formatPrice(product.price)}</span>
            <button class="btn-add-cart" data-product-id="${product.id}">Adicionar</button>
          </div>
        </div>
      </article>
    `,
    )
    .join('');
}

function updateCartDisplay() {
  const items = Object.values(cart);
  const totalQuantity = items.reduce((sum, item) => sum + item.quantity, 0);
  const totalPrice = items.reduce((sum, item) => sum + item.quantity * item.price, 0);

  cartCount.textContent = totalQuantity;
  cartTotal.textContent = formatPrice(totalPrice);

  if (items.length === 0) {
    cartItemsContainer.innerHTML = `
      <div class="empty-cart">
        <h3>Seu carrinho está vazio</h3>
        <p>Adicione perfumes para ver o total e finalizar o pedido.</p>
      </div>
    `;
    return;
  }

  cartItemsContainer.innerHTML = items
    .map(
      (item) => `
      <div class="cart-item">
        <div class="cart-item__info">
          <span class="cart-item__name">${item.name}</span>
          <span class="cart-item__price">${formatPrice(item.price)} x ${item.quantity}</span>
        </div>
        <div class="cart-item__controls">
          <button class="qty-btn" data-action="decrease" data-product-id="${item.id}">−</button>
          <span class="qty">${item.quantity}</span>
          <button class="qty-btn" data-action="increase" data-product-id="${item.id}">+</button>
          <button class="remove-btn" data-action="remove" data-product-id="${item.id}">Remover</button>
        </div>
      </div>
    `,
    )
    .join('');
}

function openCart() {
  cartSidebar.classList.add('is-open');
  cartOverlay.classList.add('is-open');
}

function closeCart() {
  cartSidebar.classList.remove('is-open');
  cartOverlay.classList.remove('is-open');
}

function showCartNotification(message) {
  const existing = document.querySelector('.cart-notification');
  if (existing) existing.remove();

  const toast = document.createElement('div');
  toast.className = 'cart-notification';
  toast.textContent = message;
  toast.addEventListener('click', () => {
    openCart();
    toast.remove();
  });

  document.body.appendChild(toast);

  setTimeout(() => {
    toast.classList.add('visible');
  }, 10);

  setTimeout(() => {
    toast.classList.remove('visible');
    setTimeout(() => toast.remove(), 300);
  }, 4200);
}

function addToCart(productId) {
  const product = products.find((item) => item.id === productId);
  if (!product) return;

  if (cart[productId]) {
    cart[productId].quantity += 1;
  } else {
    cart[productId] = { ...product, quantity: 1 };
  }

  updateCartDisplay();
  const message = `${product.name} adicionado ao carrinho. Toque para conferir e finalizar seu pedido.`;
  showCartNotification(message);
}

function setCartQuantity(productId, quantity) {
  if (!cart[productId]) return;

  if (quantity <= 0) {
    delete cart[productId];
  } else {
    cart[productId].quantity = quantity;
  }

  updateCartDisplay();
}

function removeFromCart(productId) {
  delete cart[productId];
  updateCartDisplay();
}

